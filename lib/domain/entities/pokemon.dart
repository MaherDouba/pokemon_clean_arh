class Pokemon {
  final String name;
  final String imageUrl;
  final String url;
  final int weight;
  final int height;
  final int hp;
  final int atk;
  final int def;
  final int spd;
  final int exp;

  Pokemon({
    required this.name,
    required this.imageUrl,
    required this.url,
    required this.weight,
    required this.height,
    required this.hp,
    required this.atk,
    required this.def,
    required this.spd,
    required this.exp,
  });
}
